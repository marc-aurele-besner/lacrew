/**
 * Prefer forge script JSON (includes org node accounts); fall back to
 * broadcast CREATEs. Anvil-account defaults never leak onto real chains.
 */
function loadDeploymentsForChain(chainId) {
  const file = join(repoRoot, `contracts/deployments/${chainId}.json`);
  if (existsSync(file)) {
    const data = JSON.parse(readFileSync(file, "utf8"));
    if (data.orgRegistry && data.manager) {
      return data;
    }
    // Script may have been overwritten by an older sync — merge broadcast CREATEs.
    const fromBroadcast = extractDeploymentsFromBroadcast(chainId);
    if (fromBroadcast && chainId === "31337") {
      return { ...ANVIL_NODE_DEFAULTS, ...data, ...fromBroadcast };
    }
    return fromBroadcast ? { ...data, ...fromBroadcast } : data;
  }
  const fromBroadcast = extractDeploymentsFromBroadcast(chainId);
  if (fromBroadcast && chainId === "31337") {
    return { ...ANVIL_NODE_DEFAULTS, ...fromBroadcast };
  }
  return fromBroadcast;
}

function writeDeploymentsTs() {
  mkdirSync(deploymentsDir, { recursive: true });
  const files = ["anvil.json", "31337.json", "11155111.json", "84532.json"];
  const entries = [];

  for (const file of files) {
    const path = join(deploymentsDir, file);
    if (!existsSync(path)) continue;
    const data = JSON.parse(readFileSync(path, "utf8"));
    if (typeof data.chainId !== "number") continue;
    entries.push([data.chainId, data]);
  }

  // Deduplicate by chainId (prefer higher-specificity numeric file over anvil alias).
  const byChain = new Map();
  for (const [chainId, data] of entries) {
    byChain.set(chainId, data);
  }

  const body = [
    "/** Generated by packages/core/scripts/sync-abis.mjs — do not edit by hand. */",
    "",
    'import type { ChainAddresses } from "./types.js";',
    "",
    "export const DEPLOYMENTS: Record<number, ChainAddresses> = {",
  ];

  for (const [chainId, data] of [...byChain.entries()].sort((a, b) => a[0] - b[0])) {
    body.push(`  ${chainId}: ${JSON.stringify(data, null, 2).replace(/\n/g, "\n  ")},`);
  }

  body.push("};", "");
  writeFileSync(join(coreRoot, "src/deployments.generated.ts"), body.join("\n"));
}

/**
 * A chain gets an address book when it has actually been deployed to, and not
 * before.
 *
 * This used to synthesise `0x…01`–`0x…07` entries for Base Sepolia and an anvil
 * alias. Those look like deployments: `getAddresses` returns them, the
 * orchestrator builds an "onchain" runtime from them, and every read then
 * reverts or returns zero — which the UI renders as an empty org rather than as
 * a chain nobody deployed to. That is the same failure mode as serving mock
 * data, only harder to notice, so the placeholders are gone. Callers that need
 * to know should ask `hasDeployment(chainId)`.
 *
 * `anvil.json` is still written, but only as an alias of a real 31337 deploy.
 */
function writeAnvilAlias(addresses) {
  if (addresses?.chainId !== 31337) return;
  mkdirSync(deploymentsDir, { recursive: true });
  writeFileSync(
    join(deploymentsDir, "anvil.json"),
    `${JSON.stringify(addresses, null, 2)}\n`,
  );
}

function main() {
  // No forge artifacts (Docker builds, fresh checkouts without foundry):
  // keep the committed generated files — they are the source of truth until
  // someone actually rebuilds the contracts.
  const artifactsReady = CONTRACTS.every((c) => existsSync(join(contractsOut, c.file)));
  if (!artifactsReady) {
    if (!existsSync(join(coreRoot, "src/abis.ts"))) {
      throw new Error(
        "Missing forge artifacts and no committed src/abis.ts — run: cd contracts && forge build",
      );
    }
    console.warn(
      "[sync-abis] forge artifacts missing — keeping committed abis.ts/deployments (cd contracts && forge build to regenerate)",
    );
    return;
  }

  const abis = {};
  for (const c of CONTRACTS) {
    abis[c.exportName] = loadAbi(c.file);
  }
  writeAbisTs(abis);

  for (const chainId of KNOWN_CHAIN_IDS) {
    const deployments = loadDeploymentsForChain(chainId);
    if (!deployments) continue;
    if (!deployments.orgRegistry) {
      console.warn(`Skipping chain ${chainId}: incomplete deployment JSON`);
      continue;
    }
    writeDeploymentsJson(deployments);
    writeAnvilAlias(deployments);
    console.log(`Synced deployments for chain ${deployments.chainId}`);
  }

  writeDeploymentsTs();
  console.log("Synced ABIs → packages/core/src/abis.ts");
  console.log("Synced deployments → packages/core/src/deployments.generated.ts");
}

main();
