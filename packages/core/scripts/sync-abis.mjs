function loadAbi(relPath) {
  const full = join(contractsOut, relPath);
  if (!existsSync(full)) {
    throw new Error(`Missing forge artifact: ${full}\nRun: cd contracts && forge build`);
  }
  const artifact = JSON.parse(readFileSync(full, "utf8"));
  if (!Array.isArray(artifact.abi)) {
    throw new Error(`No abi in ${full}`);
  }
  return artifact.abi;
}

function writeAbisTs(abisByExport) {
  const parts = [
    "/**",
    " * Generated by packages/core/scripts/sync-abis.mjs — do not edit by hand.",
    " * Source: contracts/out/*.sol/*.json after `forge build`.",
    " */",
    "",
  ];
  for (const [exportName, abi] of Object.entries(abisByExport)) {
    parts.push(`export const ${exportName} = ${JSON.stringify(abi, null, 2)} as const;`, "");
  }
  writeFileSync(join(coreRoot, "src/abis.ts"), parts.join("\n"));
}

const KNOWN_CHAIN_IDS = ["31337", "11155111", "84532"];

function extractDeploymentsFromBroadcast(chainId) {
  const broadcastRoot = join(repoRoot, "contracts/broadcast/DeployMockOrg.s.sol");
  const latest = join(broadcastRoot, chainId, "run-latest.json");
  if (!existsSync(latest)) return null;
  const run = JSON.parse(readFileSync(latest, "utf8"));
  const created = run.transactions?.filter((t) => t.transactionType === "CREATE") ?? [];
  const byName = {};
  for (const tx of created) {
    if (tx.contractName && tx.contractAddress) {
      byName[tx.contractName] = tx.contractAddress;
    }
  }
  if (!byName.OrgRegistry) return null;
  return {
    chainId: Number(chainId),
    mockUSDC: byName.MockUSDC ?? "0x0000000000000000000000000000000000000000",
    orgRegistry: byName.OrgRegistry,
    treasury: byName.Treasury,
    escalationRouter: byName.EscalationRouter,
    governanceModule: byName.GovernanceModule,
    spendCapPolicy: byName.SpendCapPolicy,
    policyStack: byName.PolicyStack,
    whitelistPolicy: byName.WhitelistPolicy,
    epochStreamer: byName.EpochStreamer,
    sessionRegistry: byName.SessionRegistry,
  };
}

function writeDeploymentsJson(addresses) {
  mkdirSync(deploymentsDir, { recursive: true });
  const chainFile = join(deploymentsDir, `${addresses.chainId}.json`);
  writeFileSync(chainFile, `${JSON.stringify(addresses, null, 2)}\n`);

  if (addresses.chainId === 31337) {
    writeFileSync(join(deploymentsDir, "anvil.json"), `${JSON.stringify(addresses, null, 2)}\n`);
  }

  // Also mirror under contracts/deployments for forge script consumers.
  const contractsDeploy = join(repoRoot, "contracts/deployments");
  mkdirSync(contractsDeploy, { recursive: true });
  writeFileSync(
    join(contractsDeploy, `${addresses.chainId}.json`),
    `${JSON.stringify(addresses, null, 2)}\n`,
  );
}

